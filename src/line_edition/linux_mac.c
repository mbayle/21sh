void	linux_mac2(int *ret, int *sret, char (*buf)[701])
{
	if ((*buf)[0] == 27 && (*buf)[1] == 91 && ((*buf)[2] == 72 || (*buf)[2]
	== 70 || (((*buf)[2] == 53 || (*buf)[2] == 54) && (*buf)[3] == 126)))
	{
		if ((*buf)[2] == 72 && (*ret = 3))
			(*buf)[2] = 68;
		if ((*buf)[2] == 70 && (*ret = 3))
			(*buf)[2] = 67;
		if ((*buf)[2] == 53 && (*ret = 3))
		{
			(*buf)[2] = 65;
			(*buf)[3] = '\0';
		}
		if ((*buf)[2] == 54 && (*ret = 3))
		{
			(*buf)[2] = 66;
			(*buf)[3] = '\0';
		}
		*sret = *ret;
	}
}

