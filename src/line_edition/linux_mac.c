{
	*sret = *ret;
	if (*ret == 2 && (*buf)[0] == 27 && ((*buf)[1] == 100 || (*buf)[1] == 114
	|| (*buf)[1] == 112))
	{
		if ((*buf)[1] == 100 && (*ret = 3))
		{
			(*buf)[0] = -30;
			(*buf)[1] = -120;
			(*buf)[2] = -126;
		}
		if ((*buf)[1] == 114 && (*ret = 2))
		{
			(*buf)[0] = -62;
			(*buf)[1] = -82;
		}
		if ((*buf)[1] == 112 && (*ret = 2))
		{
			(*buf)[0] = -49;
			(*buf)[1] = -128;
		}
		*sret = *ret;
	}
	else
		linux_mac2(&*ret, &*sret, &*buf);
}
