#include "libft.h"

void	free_dchar(char ***tab)
{
	int		c;

	c = 0;
	if (!tab || !*tab || !**tab)
		return ;
	while ((*tab)[c])
		free((*tab)[c++]);
	if (*tab)
		free(*tab);
}
